     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $user = User::latest();
        if (request('name')) {
            $user->where('first_name', 'like', request('name'))->orWhere('last_name', 'like', request('name'));
        }
        if (request('role')) {
            $user->where('ID_Role', request('role'));
        }
        return $user->get();
    }

